                    <h4>Penghitungan Nilai Paten dengan Pendekatan Berbasis Biaya (Cost based Approach)</h4>       
                    <br/>
                    <p>
                        Penghitungan nilai teknologi/paten menggunakan pendekatan biaya dapat dilakukan dengan menghitung biaya historis (historical cost) yang telah dikeluarkan dalam membuat dan mengembangkan suatu paten, atau biaya untuk memproduksi ulang suatu paten (reproduction cost), atau biaya pengganti untuk mengembangkan suatu paten lainnya yang sejenis (replacement cost). Dalam penilaian berbasis biaya, biaya pengembangan aset paten dianggap setara dengan nilai aset tersebut. Biaya historis yang dikeluarkan oleh pemilik paten untuk mengembangkan aset paten sama dengan harga aset saat ini. Penghitungan dengan pendekatan biaya historis merupakan biaya aktual yang terjadi dalam membuat dan mengembangkan suatu paten, yang mudah digunakan untuk menghitung aset paten pada tahap penelitian dan pengembangan.
                    </p>    
                    <p>
                        Penghitungan nilai paten dengan pendekatan biaya umumnya digunakan menilai suatu aset pada tahap awal pengembangan atau laporan keuangan dan pajak. Keuntungan menggunakan metode atau pendekatan biaya diantaranya mudah dan cepat, serta datanya obyektif dan dapat dipercaya. Namun demikian, terdapat beberapa kelemahan pada penghitungan nilai paten dengan pendekatan biaya, diantaranya mungkin sulit untuk memilah biaya yang terkait dengan aset paten dari biaya penelitian lainnya dan belum memperhitungkan manfaat ekonomi. 
                    </p>
